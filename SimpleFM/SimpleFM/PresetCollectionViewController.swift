class PresetCollectionViewController: UICollectionViewController {

    @IBAction func addPresetButtonTapped(sender: AnyObject) {
        var presetTextField: UITextField?
        let alertController = UIAlertController(title: "Create a new preset!", message: "Give your preset a name", preferredStyle: UIAlertControllerStyle.Alert)
        
        alertController.addTextFieldWithConfigurationHandler { (textField) in
            textField.placeholder = "Preset name"
            presetTextField = textField
        }
        
        let cancelAction = UIAlertAction(title: "Cancel", style: .Cancel, handler: nil)
        let createAction = UIAlertAction(title: "Create", style: .Default) {(_) in
            guard let title = presetTextField?.text where title.characters.count > 0 else {
                return
            }
        }
        alertController.addAction(cancelAction)
        alertController.addAction(createAction)
        presentViewController(alertController, animated: true, completion: nil)
    }
    
